function column(index, matrix){
  let col = [];
  matrix.forEach((row) => {col.push(row[index]);});
  return col;
}

function transpose(matrix){
  let trans = [];
  for (let i = 0; i < matrix[0].length; i++){
    trans.push(column(i, matrix));
  }
  return trans;
}
